List<BeadColor> getColorsByCategory(String category) {
  return standardColors.where((color) => color.category == category).toList();
}

List<BeadColor> getColorsByBrand(BeadBrand brand) {
  return standardColors.where((color) => color.brand == brand).toList();
}

List<String> get allCategories {
  return standardColors
      .map((color) => color.category)
      .whereType<String>()
      .toSet()
      .toList();
}

BeadColor? getColorByCode(String code) {
  try {
    return standardColors.firstWhere((color) => color.code == code);
  } catch (e) {
    return null;
  }
}
